class MatrixStack {
	int index;
	int size;
	mat4* matrices;
public:
	MatrixStack(int numMatrices = 32) : index(0), size(numMatrices) {
		matrices = new mat4[numMatrices];
	}
	~MatrixStack() {
		delete[]matrices;
	}
	mat4& push(const  mat4& m) {
		assert(index + 1 < size);
		matrices[index++] = m;
		return matrices[index];
	}
	mat4& pop(void) {
		assert(index - 1 < size);
		index--;
		return matrices[index];
	}
};
